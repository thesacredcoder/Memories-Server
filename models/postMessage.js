const { Schema, model } = require("mongoose");

const postSchema = new Schema({
  title: String,
  message: String,
  creator: String,
  name: String,
  tags: [String],
  selectedFile: String,
  likes: {
    type: [String],
    default: [],
  },
  createdAt: {
    type: Date,
    default: new Date(),
  },
});

const PostMessage = model("PostMessage", postSchema);

module.exports = PostMessage;
